CREATE TABLE CATEGORIES (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    "name" TEXT NOT NULL,
    modified TIMESTAMPTZ DEFAULT now() NOT NULL
);

CREATE TABLE BOOKMARKS (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    CATEGORY_id INTEGER REFERENCES CATEGORIES(id),
    THUMBNAIL URL TEXT,
    URL TEXT NOT NULL,
    DESCRIPTION TEXT,
    IS FAVORITE BOOLEAN,
    modified TIMESTAMPTZ DEFAULT now() NOT NULL
);