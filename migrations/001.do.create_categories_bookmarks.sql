CREATE TABLE CATEGORIES (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    "name" TEXT NOT NULL,
    modified TIMESTAMPTZ DEFAULT now() NOT NULL
);

CREATE TABLE BOOKMARKS (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    "title" TEXT, 
    category_id INTEGER REFERENCES CATEGORIES(id),
    thumbnail_url TEXT,
    link TEXT NOT NULL,
    "description" TEXT,
    is_favorite BOOLEAN,
    modified TIMESTAMPTZ DEFAULT now() NOT NULL
);